/* ************************************************************************** */
/*                                                                            */
/*  FUNCTION: ft_strtrim                                                      */
/*                                                                            */
/*  Allocates memory (using malloc(3)) and returns a copy of ’s1’ with        */
/*  characters from ’set’ removed from the beginning and the end.             */
/*                                                                            */
/*  @param s1  The string to be trimmed                						  */
/*  @param set  The string containing the set of characters to be removed     */
/*  @return       The trimmed string / NULL if the allocation fails           */
/*                                                                            */
/*  Example:                                                                  */
/*     int *arr = ft_calloc(10, sizeof(int));                                 */
/*                                                                            */
/* ************************************************************************** */

#include "includes/libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	len;
	size_t	start;
	size_t	end;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	len = end - start;
	return (ft_substr(s1, start, len));
}
