/* ************************************************************************** */
/*                                                                            */
/*  FUNCTION: ft_lstmap                                                       */
/*                                                                            */
/*  Iterates through the list ’lst’, applies the function ’f’ to each node’s  */
/* 	content, and creates a new list resulting of the successive applications  */
/* 	of the function ’f’. The ’del’ function is used to delete the content of  */
/* 	a node if needed.                                                         */
/*                                                                            */
/*  @param lst  The address of a pointer to a node.                           */
/*  @param f    The address of the function applied to each node’s content.   */
/*  @param del  The address of the function used to delete a node’s           */
/*				content if needed                                             */
/*  @return     The new list / NULL if allocation fails.                      */
/*                                                                            */
/*  Example:                                                                  */
/*     int *arr = ft_calloc(10, sizeof(int));                                 */
/*                                                                            */
/* ************************************************************************** */

#include "includes/libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_node;
	t_list	*new_list;
	void	*new_content;

	if (!lst || !f || !del)
		return (NULL);
	new_list = NULL;
	while (lst)
	{
		new_content = f(lst->content);
		new_node = ft_lstnew(new_content);
		if (!new_node)
		{
			del(new_content);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
