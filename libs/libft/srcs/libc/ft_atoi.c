/* ************************************************************************** */
/*                                                                            */
/*  FUNCTION: ft_atoi                                                         */
/*                                                                            */
/*  Convertit une chaine representant un nombre en entier signe.              */
/*                                                                            */
/*  @param str  Chaine a analyser.                                            */
/*  @return  Entier signe converti.                                           */
/*                                                                            */
/*  int value = ft_atoi("  -42");                                             */
/*                                                                            */
/* ************************************************************************** */

#include "includes/libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	result;

	i = 0;
	sign = 1;
	result = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * sign);
}
