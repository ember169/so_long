/* ************************************************************************** */
/*                                                                            */
/*  FUNCTION: ft_strchr                                                       */
/*                                                                            */
/*  Cherche la premiere occurrence d'un caractere dans une chaine C.          */
/*                                                                            */
/*  @param s  Chaine source.                                                  */
/*  @param c  Caractere recherche.                                            */
/*  @return  Adresse du caractere trouve ou NULL.                             */
/*                                                                            */
/*  char *slash = ft_strchr(path, '/');                                       */
/*                                                                            */
/* ************************************************************************** */

#include "includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (NULL);
}
